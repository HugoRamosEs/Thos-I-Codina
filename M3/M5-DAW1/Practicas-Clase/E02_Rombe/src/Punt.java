
class Punt {
	// ATRIBUTS
	double x;
	double y;
	
	// CONSTRUCTORS
	Punt(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	// GETTERS I SETTERS
	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}
	
}
